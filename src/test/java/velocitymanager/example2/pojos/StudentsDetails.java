package velocitymanager.example2.pojos;

/**
 * Student POJO class
 * Student can be from Maths or Science Dept. only
 */
public class StudentsDetails {

	String studRollNo;
	String studFirstName;
	String studLastName;
	String studDepartment;
	String studSchoolName;
	String studClassTeacher;
	String studCity;
	
	public String getStudRollNo() {
		return studRollNo;
	}
	public void setStudRollNo(String studRollNo) {
		this.studRollNo = studRollNo;
	}
	public String getStudFirstName() {
		return studFirstName;
	}
	public void setStudFirstName(String studFirstName) {
		this.studFirstName = studFirstName;
	}
	public String getStudLastName() {
		return studLastName;
	}
	public void setStudLastName(String studLastName) {
		this.studLastName = studLastName;
	}
	public String getStudDepartment() {
		return studDepartment;
	}
	public void setStudDepartment(String studDepartment) {
		this.studDepartment = studDepartment;
	}
	public String getStudSchoolName() {
		return studSchoolName;
	}
	public void setStudSchoolName(String studSchoolName) {
		this.studSchoolName = studSchoolName;
	}
	public String getStudClassTeacher() {
		return studClassTeacher;
	}
	public void setStudClassTeacher(String studClassTeacher) {
		this.studClassTeacher = studClassTeacher;
	}
	public String getStudCity() {
		return studCity;
	}
	public void setStudCity(String studCity) {
		this.studCity = studCity;
	}

	
	

	
	
	
}
