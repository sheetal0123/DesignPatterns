package velocitymanager.example2.pojos;

public class ScienceDepartmentHeader {

	String deptName;
	String deptHOD;
	String deptFacultyCount;
	String deptStartedYear;
	
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getDeptHOD() {
		return deptHOD;
	}
	public void setDeptHOD(String deptHOD) {
		this.deptHOD = deptHOD;
	}
	public String getDeptFacultyCount() {
		return deptFacultyCount;
	}
	public void setDeptFacultyCount(String deptFacultyCount) {
		this.deptFacultyCount = deptFacultyCount;
	}
	public String getDeptStartedYear() {
		return deptStartedYear;
	}
	public void setDeptStartedYear(String deptStartedYear) {
		this.deptStartedYear = deptStartedYear;
	}


}
